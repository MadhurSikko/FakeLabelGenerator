def _gen_A(rng):
    """[cation] [acid-base]-ate  — mirrors INS 202, 211, 282"""
    cation = rng.choice(["sodium", "potassium", "calcium", "magnesium", "ammonium"])
    base = rng.choice(ACID_BASES)
    return f"{cation} {base}"


def _gen_B(rng):
    """[modifier]-[base] [acid]  — mirrors INS 472e (diacetyl tartaric acid)"""
    mod = rng.choice(ACID_MODIFIERS)
    acid = rng.choice(ACID_NAMES)
    return f"{mod} {acid} acid"


def _gen_C(rng):
    """[cation] [acyl]oyl [end]  — mirrors INS 481, 482"""
    cation = rng.choice(["sodium", "potassium", "calcium", "magnesium"])
    acyl = rng.choice(ACYL_BASES)
    end = rng.choice(ACYL_ENDS)
    return f"{cation} {acyl} {end}"


def _gen_D(rng):
    """[acid] esters of mono- and diglycerides  — mirrors INS 472a-f"""
    acid = rng.choice(ESTER_ACIDS)
    return f"{acid} esters of mono- and diglycerides of fatty acids"


def _gen_E(rng):
    """[modifier][polymer] [end]  — mirrors INS 1422, 1442"""
    mod = rng.choice(POLYMER_MODS)
    base = rng.choice(POLYMER_BASES)
    end = rng.choice(POLYMER_ENDS)
    return f"{mod} {base} {end}"


def _gen_F(rng):
    """polysorbate [N]  — mirrors INS 432, 435, 436, 433"""
    n = rng.choice(POLY_NUMBERS)
    return f"polysorbate {n}"


def _gen_G(rng):
    """[di/tri][cation] [base]  — mirrors INS 627, 631, 635"""
    cation = rng.choice(MULTI_CATIONS)
    base = rng.choice(MULTI_BASES)
    return f"{cation} {base}"


def _gen_H(rng):
    """[hydroxy/alkyl]-[X]cellulose  — mirrors INS 464, 466"""
    p1 = rng.choice(CELLU_PREFIX1)
    p2 = rng.choice(CELLU_PREFIX2)
    # Vary structure: sometimes [p1][p2]cellulose, sometimes [p1] [p2]cellulose
    if rng.random() < 0.5:
        return f"{p1} {p2}cellulose"
    else:
        return f"{p1}{p2}cellulose"


