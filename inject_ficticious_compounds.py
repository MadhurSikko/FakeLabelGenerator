# Main
# ---------------------------------------------------------------------------


def process_file(input_path, seed=42):
    records = []
    with open(input_path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                records.append(json.loads(line))

    print(f"Loaded {len(records)} records from '{input_path}'")

    rng = random.Random(seed)
    used_ins = set()  # track across records so no two records share an INS number

    stem, _ = os.path.splitext(input_path)
    out_path = stem + "_fictitious_compounds.jsonl"

    injected = 0
    skipped = 0
    pattern_counts = {}

    with open(out_path, "w", encoding="utf-8") as out_f:
        for rec in records:
            processed = process_record(rec, rng, used_ins)
            out_f.write(json.dumps(processed, ensure_ascii=False) + "\n")
            if processed["fictitious_compound_injected"]:
                injected += 1
                p = processed["fictitious_compound_changes"]["naming_pattern"]
                pattern_counts[p] = pattern_counts.get(p, 0) + 1
            else:
                skipped += 1

    print(f"  Compounds injected  : {injected}")
    print(f"  Skipped (too short) : {skipped}")
    print(f"  Pattern distribution: {dict(sorted(pattern_counts.items()))}")
    print(f"  Output: {out_path}")


if __name__ == "__main__":
    if len(sys.argv) < 2:
        print("Usage: python inject_fictitious_compounds.py <input.jsonl>")
        sys.exit(1)
    path = sys.argv[1]
    if not os.path.isfile(path):
        print(f"File not found: {path}")
        sys.exit(1)
    process_file(path)
