def generate_compound(category, rng, used_ins):
    """
    Pick a pattern appropriate for the category, generate a name,
    assign a fictitious INS number not already used this file.
    Returns (compound_name, ins_code, pattern, func_class).
    """
    weights_dict = CATEGORY_PATTERN_WEIGHTS.get(
        category, CATEGORY_PATTERN_WEIGHTS["generic"]
    )
    patterns = sorted(weights_dict.keys())
    weights = [weights_dict[p] for p in patterns]
    pattern = rng.choices(patterns, weights=weights)[0]

    compound_name = PATTERN_GENERATORS[pattern](rng)

    # Pick a fictitious INS number not used this run
    available = sorted(set(FICTITIOUS_INS_NUMBERS) - used_ins)
    ins_code = rng.choice(available)
    used_ins.add(ins_code)

    # Pick a contextually appropriate functional class
    func_class = rng.choice(PATTERN_CLASS_MAP[pattern])

    return compound_name, ins_code, pattern, func_class


def process_record(record, rng, used_ins):
    rec = copy.deepcopy(record)
    rec.pop("fictitious_compound_injected", None)
    rec.pop("fictitious_compound_changes", None)

    label_text = rec.get("raw_label_text", "").strip()
    if len(label_text) < 10:
        rec["fictitious_compound_injected"] = False
        rec["fictitious_compound_changes"] = {
            "ins_code": None,
            "compound_name": None,
            "functional_class": None,
            "pattern": None,
            "char_index": None,
            "explanation": "Label text too short to inject a compound.",
        }
        return rec

    category = detect_category(label_text.lower())
    compound_name, ins_code, pattern, func_class = generate_compound(
        category, rng, used_ins
    )

    fragment = _build_label_fragment(compound_name, ins_code, func_class, rng)
    new_text, char_index = _inject_into_label(label_text, fragment)
    rec["raw_label_text"] = new_text

    rec["fictitious_compound_injected"] = True
    rec["fictitious_compound_changes"] = {
        "ins_code": ins_code,
        "compound_name": compound_name,
        "full_fragment": fragment,
        "functional_class": func_class,
        "naming_pattern": pattern,
        "detected_category": category,
        "char_index": char_index,
        "explanation": (
            f"Fictitious compound '{compound_name}' (INS {ins_code}) injected "
            f"using naming Pattern {pattern}, which mirrors real INS compound "
            f"grammar. The INS number {ins_code} is unassigned in the official "
            f"Codex Alimentarius list. Detected product category: '{category}'."
        ),
    }
    return rec


