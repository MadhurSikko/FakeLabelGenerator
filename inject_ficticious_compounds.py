# Label injection formats
# ---------------------------------------------------------------------------


def _inject_into_label(label_text, compound_str):
    """Splice before CONTAINS / ADDED FLAVOUR / ADDED COLOUR, else append."""
    m = re.search(
        r"(CONTAINS\b|Contains\b|ADDED FLAVOUR|ADDED COLOR|ADDED COLOUR|"
        r"PERMITTED NATURAL|PERMITTED SYNTHETIC|Numbers in brackets)",
        label_text,
        re.IGNORECASE,
    )
    if m:
        pos = m.start()
        before = label_text[:pos].rstrip(", \r\n")
        after = label_text[pos:]
        new = before + ", " + compound_str + " " + after
        return new, len(before) + 2
    else:
        stripped = label_text.rstrip(". \r\n")
        new = stripped + ", " + compound_str
        return new, len(stripped) + 2


def _build_label_fragment(compound_name, ins_code, func_class, rng):
    """
    Build the full label string in one of three authentic Indian formats:

    Format 1 (most common):  FUNCTIONAL CLASS [NAME (INS XXXX)]
    Format 2 (common):       FUNCTIONAL CLASS (NAME, INS XXXX)
    Format 3 (less common):  NAME (INS XXXX) [FUNCTIONAL CLASS]
    """
    fmt = rng.choices([1, 2, 3], weights=[45, 35, 20])[0]
    display = compound_name.upper()
    if fmt == 1:
        return f"{func_class} [{display} (INS {ins_code})]"
    elif fmt == 2:
        return f"{func_class} ({display}, INS {ins_code})"
    else:
        return f"{display} (INS {ins_code}) [{func_class}]"


