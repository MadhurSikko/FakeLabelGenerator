"""
inject_fictitious_compounds.py
-------------------------------
Injects one fictitious compound into `raw_label_text` per record.

The fictitious compound is generated by a grammar that mirrors the exact
naming conventions of real INS / Codex Alimentarius compounds:

  Pattern A  [cation] [acid-base]-ate
             e.g. "dipotassium fumarate", "calcium erythorbate"
             mirrors: sodium benzoate (211), potassium sorbate (202)

  Pattern B  [modifier]-[base] [acid]
             e.g. "dihydroxypropyl succinic acid", "hydroxyethyl tartaric acid"
             mirrors: glucono delta-lactone (575), diacetyl tartaric acid (472e)

  Pattern C  [cation] [base]oyl [end]
             e.g. "magnesium stearoyl fumarate", "sodium palmitoyl lactylate"
             mirrors: sodium stearoyl lactylate (481), calcium stearoyl lactylate (482)

  Pattern D  [acid] esters of [glyceride-base]
             e.g. "succinic acid esters of mono- and diglycerides of fatty acids"
             mirrors: acetic acid esters of mono- and diglycerides (472a),
                      lactic acid esters of mono- and diglycerides (472b)

  Pattern E  [modifier][polymer-base] [treatment]
             e.g. "propylated distarch fumarate", "butyrylated hydroxypropyl starch"
             mirrors: acetylated distarch adipate (1422),
                      hydroxypropyl distarch phosphate (1442)

  Pattern F  poly[base]-[number]
             e.g. "polysorbate 45", "polysorbate 35"
             mirrors: polysorbate 20 (432), polysorbate 60 (435), polysorbate 80 (433)

  Pattern G  [multiplier][cation] [base]ate
             e.g. "dipotassium succinate", "tricalcium malate"
             mirrors: disodium guanylate (627), disodium inosinate (631)

  Pattern H  [hydroxy/alkyl]-[base]cellulose
             e.g. "hydroxybutyl methylcellulose", "ethylhydroxypropyl cellulose"
             mirrors: hydroxypropyl methylcellulose (464),
                      sodium carboxymethylcellulose (466)

Each generated compound:
  - sounds like a genuine INS name (same morpheme vocabulary, same grammar)
  - is assigned a plausible fictitious INS number (gap in real sequence)
  - is injected in authentic Indian label format:
      FUNCTIONAL CLASS [NAME (INS XXXX)]        -- most common
      FUNCTIONAL CLASS (NAME, INS XXXX)         -- common variant
      NAME (INS XXXX) [FUNCTIONAL CLASS]        -- less common

Output adds:
  "fictitious_compound_injected": true | false
  "fictitious_compound_changes": {
      "ins_code":         fictitious INS number string
      "compound_name":    generated name
      "functional_class": FSSAI class title
      "pattern":          A-H (which naming grammar was used)
      "char_index":       position in modified raw_label_text
      "explanation":      human-readable note
  }

Usage:
    python inject_fictitious_compounds.py <input.jsonl>

Output:
    <stem>_fictitious_compounds.jsonl
"""

